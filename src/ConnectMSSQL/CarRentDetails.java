    public CarRentDetails(String carRentID,String userId) {
        initComponents();
        
        carRentId=carRentID;
        uid=userId;
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=PROJECT;user=sa;password=123456";
            Connection con = DriverManager.getConnection(url);

            String sql = "Select Car.CarBrand,Car.CarModel,Car.CarType,Car.RentPrice,ServiceProvider.FirstName+' '+ServiceProvider.LastName as spName,ServiceProvider.Email,ServiceProvider.Phone from Car inner join CarRent on CarRent.CarRentId=Car.CarRentId inner join ServiceProvider on ServiceProvider.ServiceProviderId=CarRent.ServiceProviderId where Car.CarRentId=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, carRentId);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                System.out.println(carRentId);
                carBrand.setText(rs.getString("CarBrand"));
                carModel.setText(rs.getString("CarModel"));
                carType.setText(rs.getString("CarType"));
                rentPrice.setText(rs.getString("RentPrice"));
                spName.setText(rs.getString("spName"));
                email.setText(rs.getString("Email"));
                phone.setText(rs.getString("Phone"));
                
            }
            else{
                JOptionPane.showMessageDialog(null, "error");
                
            }
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

