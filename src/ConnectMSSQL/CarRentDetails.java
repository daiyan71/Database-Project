        // TODO add your handling code here:
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Calendar calobj = Calendar.getInstance();
        String date;
        date=(df.format(calobj.getTime())).toString();
        
        System.out.println(date);
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=PROJECT;user=sa;password=123456";
            Connection con = DriverManager.getConnection(url);
            String sql = "Insert into OrderTable"
                    +"(CarRentId,CarServiceId,UserId,amount,DateAndTime)"
                    +"values(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(carRentId));
            pst.setInt(3, Integer.parseInt(uid));
            pst.setString(4, rentPrice.getText());
            pst.setInt(2, 0);
            pst.setString(5, date);

            pst.executeUpdate();
        
            JOptionPane.showMessageDialog(null, "Successfully Ordered");
            
            setVisible(false);
            UserHome s = new UserHome(uid);
            s.setVisible(true);

            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
